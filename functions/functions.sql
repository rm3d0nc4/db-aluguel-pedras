CREATE OR REPLACE FUNCTION LANCAR_MULTAS() RETURNS VOID AS $$
    BEGIN
        INSERT INTO MULTA (DESCRICAO, VALOR_MULTA, ID_TIPO_MULTA, ID_PEDIDO)
        SELECT 'ATRASO NO PAGAMENTO DA COMISSÃO', (SELECT VALOR_MULTA FROM TIPO_MULTA WHERE ID_TIPO_MULTA = 2), 2, ID_PEDIDO FROM PEDIDO WHERE DATA_VENCIMENTO_PAGAMENTO_COMISSAO < NOW() AND NOT EXISTS (SELECT * FROM MULTA MU WHERE MU.ID_PEDIDO = ID_PEDIDO AND MU.ID_TIPO_MULTA = 2) AND (DATA_PAGAMENTO_COMISSAO IS NULL OR DATA_PAGAMENTO_COMISSAO > DATA_VENCIMENTO_PAGAMENTO_COMISSAO);
    END;
$$ LANGUAGE 'plpgsql';
