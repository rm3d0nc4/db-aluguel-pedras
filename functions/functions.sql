CREATE OR REPLACE FUNCTION MULTAR_PEDIDO(_ID_PEDIDO INT, _DESCRICAO TEXT, _ID_TIPO_MULTA INT) 
RETURNS SETOF MULTA AS $$
	DECLARE VAR_ID_MULTA INT;
	DECLARE VAR_VALOR_MULTA NUMERIC(10,2);
	BEGIN

		PERFORM PRIVATE_VERIFICAR_EXISTENCIA_PEDIDO(_ID_PEDIDO);

		IF (SELECT DATA_PAGAMENTO_PEDIDO FROM PEDIDO WHERE ID_PEDIDO = _ID_PEDIDO) IS NULL THEN
			RAISE EXCEPTION 'PEDIDO NÚMERO % NÃO FOI EFETIVADO', _ID_PEDIDO;
		END IF;
		SELECT VALOR_MULTA INTO VAR_VALOR_MULTA FROM TIPO_MULTA WHERE ID_TIPO_MULTA = _ID_TIPO_MULTA;
		INSERT INTO MULTA VALUES(DEFAULT, _DESCRICAO, _ID_PEDIDO, VAR_VALOR_MULTA, DEFAULT, _ID_TIPO_MULTA) 
		RETURNING ID_MULTA INTO VAR_ID_MULTA;
		
		RAISE NOTICE 'MULTA REGISTRADA';
		RETURN QUERY SELECT * FROM MULTA WHERE ID_MULTA = VAR_ID_MULTA;
		
	END;
$$ LANGUAGE 'plpgsql';



/*
=====================================
||           Função para       	   ||
||      Verificar Multas Ativas    ||
||                                 ||
=====================================
*/



CREATE OR REPLACE FUNCTION VERIFICAR_MULTAS(_CPF_CNPJ TEXT) 
RETURNS SETOF MULTA AS $$
	DECLARE VAR_ID_PARCEIRO INT;
	BEGIN
		PERFORM LANCAR_MULTAS();
		IF EXISTS (SELECT ID_PARCEIRO FROM PARCEIRO WHERE CPF_CNPJ = _CPF_CNPJ) THEN
			SELECT ID_PARCEIRO INTO VAR_ID_PARCEIRO FROM PARCEIRO WHERE CPF_CNPJ = _CPF_CNPJ;
			IF EXISTS (SELECT MULTA.* FROM MULTA NATURAL JOIN PEDIDO WHERE ID_PARCEIRO = VAR_ID_PARCEIRO AND DATA_PAGAMENTO_MULTA ISNULL) THEN
				RETURN QUERY SELECT MULTA.* FROM MULTA NATURAL JOIN PEDIDO WHERE ID_PARCEIRO = VAR_ID_PARCEIRO AND DATA_PAGAMENTO_MULTA ISNULL;
			ELSE
				RAISE NOTICE 'NÃO HÁ MULTAS PARA O PARCEIRO SELECIONADO';
			END IF;

		ELSE
			RAISE EXCEPTION 'PARCEIRO NÃO EXISTE';
		END IF; 
	END;

$$ LANGUAGE 'plpgsql';

/*
=================================
||          Função para        ||
||          Pagar Multa        ||
=================================
*/

CREATE OR REPLACE FUNCTION PAGAR_MULTA(_ID_MULTA INT) RETURNS SETOF MULTA AS $$
	DECLARE
	VAR_ID_TIPO_MULTA INT;
	VAR_ID_PEDIDO INT;
	VAR_DATA_PAGAMENTO_COMISSAO TIMESTAMP;
	BEGIN
		IF EXISTS (SELECT * FROM MULTA WHERE ID_MULTA = _ID_MULTA) THEN
			IF (SELECT DATA_PAGAMENTO_MULTA FROM MULTA WHERE ID_MULTA = _ID_MULTA) IS NOT NULL THEN
				RAISE EXCEPTION 'A MULTA Nº % JÁ FOI PAGA', _ID_MULTA;
			END IF;

			SELECT ID_TIPO_MULTA, ID_PEDIDO INTO VAR_ID_TIPO_MULTA, VAR_ID_PEDIDO FROM MULTA WHERE ID_MULTA = _ID_MULTA;
			SELECT DATA_PAGAMENTO_COMISSAO INTO VAR_DATA_PAGAMENTO_COMISSAO FROM PEDIDO WHERE ID_PEDIDO = VAR_ID_PEDIDO;

			IF VAR_DATA_PAGAMENTO_COMISSAO IS NULL AND VAR_ID_TIPO_MULTA = 1 THEN 
				RAISE EXCEPTION 'PARA PAGAR A MULTA Nº %, A COMISSÃO PEDIDO Nº % DEVE SER PAGO', _ID_MULTA, VAR_ID_PEDIDO;
			END IF;

			UPDATE MULTA SET DATA_PAGAMENTO_MULTA = NOW() WHERE ID_MULTA = _ID_MULTA;
			RAISE NOTICE 'MULTA PAGA COM SUCESSO!';
			RETURN QUERY SELECT * FROM MULTA WHERE ID_MULTA = _ID_MULTA;

		ELSE 
			RAISE EXCEPTION 'MULTA Nº % NÃO EXISTE', _ID_MULTA;
		END IF;
	END;


$$ LANGUAGE 'plpgsql';


/*
=====================================
||           Função para       	   ||
||         Cancelar pedido         ||
||                                 ||
=====================================
*/

CREATE OR REPLACE FUNCTION CANCELAR_PEDIDO(_ID_PEDIDO INT) RETURNS VOID AS $$
	BEGIN
		PERFORM PRIVATE_VERIFICAR_EXISTENCIA_PEDIDO(_ID_PEDIDO);
		PERFORM VALIDAR_MOVIMENTACAO_PEDIDO(_ID_PEDIDO);

		IF (SELECT DATA_PAGAMENTO_PEDIDO FROM PEDIDO WHERE ID_PEDIDO = _ID_PEDIDO) IS NOT NULL THEN
			RAISE EXCEPTION 'PEDIDO NÚMERO % JÁ FOI PAGO', _ID_PEDIDO;
		END IF;

		DELETE FROM LISTA_PEDRAS_PEDIDO WHERE ID_PEDIDO = _ID_PEDIDO;
		DELETE FROM PEDIDO WHERE ID_PEDIDO = _ID_PEDIDO;
		RAISE NOTICE 'PEDIDO CANCELADO COM SUCESSO!';
	END;
$$ LANGUAGE 'plpgsql';




