CREATE OR REPLACE FUNCTION VALIDAR_MOVIMENTACAO_PEDIDO(_ID_PEDIDO INT) RETURNS VOID AS $$
	BEGIN
		IF (SELECT ID_ANALISTA  FROM PEDIDO WHERE ID_PEDIDO = _ID_PEDIDO) != (SELECT ID_ANALISTA FROM ANALISTA WHERE USERNAME = CURRENT_USER) AND CURRENT_USER != 'postgres' THEN
				RAISE EXCEPTION 'APENAS % PODE MOVIMENTAR O PEDIDO', (SELECT NOME FROM ANALISTA NATURAL JOIN PEDIDO WHERE ID_PEDIDO = _ID_PEDIDO);
		END IF;
	END;
$$ LANGUAGE 'plpgsql';

/*
========================================
||                                    ||
||       Função para Adicionar        ||
||          Pedra ao Pedido           ||
========================================
*/

CREATE OR REPLACE FUNCTION ADD_ITEM(
	_ID_PEDRA INT, 
	_DATA_INICIAL DATE, 
	_DATA_FINAL DATE,
	_ID_PEDIDO INT DEFAULT NULL,
	_CPF_CNPJ TEXT DEFAULT NULL
	) RETURNS SETOF LISTA_PEDRAS_PEDIDO AS $$
	DECLARE
	VAR_ID_ANALISTA INT;
	VAR_ID_PARCEIRO INT;
	VAR_ID_PEDIDO INT;

	BEGIN
		PERFORM LANCAR_MULTAS();
		
		SELECT ID_ANALISTA INTO VAR_ID_ANALISTA FROM ANALISTA WHERE USERNAME = CURRENT_USER; -- ADICONAR CONTROLE DE USUARIO

		SELECT ID_PARCEIRO INTO VAR_ID_PARCEIRO FROM PARCEIRO WHERE CPF_CNPJ = _CPF_CNPJ;

		IF _ID_PEDRA NOT IN (SELECT ID_PEDRA FROM VER_PEDRAS_DISPONIVEIS(_DATA_INICIAL, _DATA_FINAL)) THEN
			RAISE EXCEPTION 'A PEDRA NÃO ESTÁ DISPONÍVEL NO INTERVALO ESCOLHIDO';
		END IF;

		IF _ID_PEDIDO IS NULL THEN
			IF EXISTS (SELECT * FROM VERIFICAR_MULTAS(_CPF_CNPJ)) THEN
				RAISE EXCEPTION 'HÁ MULTAS PENDENTES PARA O CPF/CNPJ %. PAGUE AS MULTAS E TENTE NOVAMENTE', _CPF_CNPJ;
			END IF;
			SELECT PRIVATE_CRIAR_PEDIDO INTO VAR_ID_PEDIDO FROM PRIVATE_CRIAR_PEDIDO(VAR_ID_PARCEIRO, VAR_ID_ANALISTA);
		END IF;

		PERFORM VALIDAR_MOVIMENTACAO_PEDIDO(COALESCE(_ID_PEDIDO, VAR_ID_PEDIDO));

		INSERT INTO LISTA_PEDRAS_PEDIDO VALUES (DEFAULT, _ID_PEDRA, COALESCE(_ID_PEDIDO, VAR_ID_PEDIDO), _DATA_INICIAL, _DATA_FINAL);
		RAISE NOTICE 'PEDRA ADICIONADA AO PEDIDO Nº %',VAR_ID_PEDIDO;

		RETURN QUERY SELECT * FROM LISTA_PEDRAS_PEDIDO WHERE ID_PEDIDO = COALESCE(_ID_PEDIDO, VAR_ID_PEDIDO);
	END;
$$ LANGUAGE 'plpgsql';


